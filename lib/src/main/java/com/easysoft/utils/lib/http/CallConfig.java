package com.easysoft.utils.lib.http;

public class CallConfig {
    String type;
    String mResponseCharset;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getmResponseCharset() {
        return mResponseCharset;
    }

    public void setmResponseCharset(String mResponseCharset) {
        this.mResponseCharset = mResponseCharset;
    }
}
